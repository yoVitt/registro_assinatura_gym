    private void liberaCampos(boolean a){
        nome_ass.setEnabled(a);
        logra_ass.setEnabled(a);
        bairro_ass.setEnabled(a);
        telEmer_ass.setEnabled(a);
        tel_ass.setEnabled(a);
        tipo_assinatura.setEnabled(a);
        dataIni_ass.setEnabled(a);
    }
    private void limpaCampos(){
        nome_ass.setText("");
        logra_ass.setText("");
        bairro_ass.setText("");
        telEmer_ass.setText("");
        tel_ass.setText("");
        dataIni_ass.setText("");
    }
    private void liberaBotoes(boolean a, boolean b, boolean c, boolean d, boolean e){
        btnNovo.setEnabled(a);
        btnSalvar.setEnabled(b);
        btnCancelar.setEnabled(c);
        btnExcluir.setEnabled(d);
        btnSair.setEnabled(e);
    }
    private void gravar(){
        try{
            assinanteDTO.setNome_ass(nome_ass.getText());
            assinanteDTO.setLogra_ass(logra_ass.getText());
            assinanteDTO.setBairro_ass(bairro_ass.getText());
            assinanteDTO.setTelEmer_ass(Integer.parseInt(telEmer_ass.getText()));
            assinanteDTO.setTel_ass(Integer.parseInt(tel_ass.getText()));
            assinanteDTO.setDataIni_ass(dataIni_ass.getText());
            assinanteDTO.setTipo_assinatura(tipo_assinatura.getSelectedItem().toString());
            
            JOptionPane.showMessageDialog(null, assinanteCTR.inserirAssinante(assinanteDTO));
        }catch(Exception e){
            System.out.println("Erro ao Gravar" + e.getMessage());
        }
    }
    private void preencheCampos(int id_ass){
        try{
            assinanteDTO.setId_ass(id_ass);
            rs = assinanteCTR.consultarAssinante(assinanteDTO,2);
            if(rs.next()){
                limpaCampos();
                tipo_assinatura.repaint();
                tipo_assinatura.revalidate();

                
                nome_ass.setText(rs.getString("nome_ass"));
                logra_ass.setText(rs.getString("logra_ass"));
                bairro_ass.setText(rs.getString("bairro_ass"));
                dataIni_ass.setText(rs.getString("dataIni_ass"));
                tipo_assinatura.setSelectedItem(rs.getString("tipo_assinatura"));
                telEmer_ass.setText(rs.getString("telEmer_ass"));
                tel_ass.setText(rs.getString("tel_ass"));
                
                gravar_alterar = 2;
                liberaCampos(true);
            }
        }catch(Exception erTab){
            System.out.println("Erro SQL: "+erTab);
        }finally{
            assinanteCTR.CloseDB();
        }
    }
    private void alterar(){
        try{
            assinanteDTO.setNome_ass(nome_ass.getText());
            assinanteDTO.setLogra_ass(logra_ass.getText());
            assinanteDTO.setBairro_ass(bairro_ass.getText());
            assinanteDTO.setDataIni_ass(dataIni_ass.getText());
            assinanteDTO.setTelEmer_ass(Integer.parseInt(telEmer_ass.getText()));
            assinanteDTO.setTel_ass(Integer.parseInt(tel_ass.getText()));
            assinanteDTO.setTipo_assinatura(tipo_assinatura.getSelectedItem().toString());
            
            JOptionPane.showMessageDialog(null, assinanteCTR.alterarAssinante(assinanteDTO));
        }catch(Exception e){
            System.out.println("Erro ao Alterar "+e.getMessage());
        }
    }
        private void excluir(){
        if(JOptionPane.showConfirmDialog(null, "Deseja Realmente excluir o Cliente?","Aviso",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null, 
                    assinanteCTR.excluirAssinante(assinanteDTO)
            );
        }
    }
    
