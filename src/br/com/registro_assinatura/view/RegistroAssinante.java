    private void preencherTabela(String nome_ass){
        try{
            modelo_jtl_consultar_assinante.setNumRows(0);
            assinanteDTO.setNome_ass(nome_ass);
            rs=assinanteCTR.consultarAssinante(assinanteDTO, 1);
            while(rs.next()){
                modelo_jtl_consultar_assinante.addRow(new Object[]{
                    rs.getString("id_ass"),
                    rs.getString("nome_ass"),
                });
            }
        }catch(Exception erTab){
            System.out.println("Erro SQL: "+erTab);
        }finally{
            assinanteCTR.CloseDB();
        }
    }

